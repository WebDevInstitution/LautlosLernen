<details>
  <summary>Was ist Gebärdensprache?</summary>
  <p>Gebärdensprache ist eine visuelle Sprache, die von gehörlosen und schwerhörigen Menschen verwendet wird, um zu kommunizieren. Sie basiert auf Handbewegungen, Gesten, Mimik und Körperhaltung und hat ihre eigene Grammatik und Syntax. Allein die Deutsche Gebärdensprache umfasst ca. 18.000 Gebärden, woraus in Kombination mit Mimik und Kopf- oder Oberkörperhaltung einen Wortschatz von ca. 250.000 Ausdrücken ermöglichen.</p>
</details>

<details>
  <summary>Wie viele verschiedene Gebärdensprachen gibt es?</summary>
  <p> Es gibt rund 200 Gebärdensprachen auf der ganzen Welt, wobei jede Region oder jedes Land ihre eigene Gebärdensprache hat. Wie bei verbaler Sprache gibt es auch in der Gebärdensprache Dialekte. Beispiele sind American Sign Language (ASL) in den USA, British Sign Language (BSL) im Vereinigten Königreich und Deutsche Gebärdensprache (DGS) in der deutschsprachigen Region.</p>
</details>

<details>
  <summary>Ist Gebärdensprache universell?</summary>
  <p>Nein, Gebärdensprache ist nicht universell. Wie gesprochene Sprachen unterscheidet sie sich je nach Region und Kultur. Jede Gebärdensprache hat ihre eigenen Grammatikregeln, Vokabular und Ausdrucksformen.</p>

</details>

